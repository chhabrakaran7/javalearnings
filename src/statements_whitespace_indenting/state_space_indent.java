// Statements, White space and indenting //

/*Statement:
* Anything from the beginning of code until the semicolon ";" is called a statement
Whitespace:
* A whitespace is the empty space between any two Variables/operators/operands/values etc.
Indentation:
* The indentation is a process to make the code more readable
 */

package statements_whitespace_indenting;

public class state_space_indent {
    public static void main(String[] args) {

        int myvariable = 50;
        // Statement is : "int myvariable = 50;"
        // Whitespace is : "int<WHITESPACE>myvariable<WHITESPACE>=<WHITESPACE>50;"
        // Indentation is: the formatting of code within the class

    }
}
