/*
KEYWORDS:
1. Keywords are reserved words in java, stored in blue/orange.
2. There are 53 reserved keywords.
3. Few of the keywords: "int", "package", "public", "static" etc.
EXPRESSIONS:
1. Building blocks of a java program.
2. Expression = VARIABLE, operators and values.
3. Ex: double kilometres = (100 * 1.60934);
    ---> Expression (above statement) : "kilometres = (100 * 1060934)"
 */
package keywords_expressions;

public class expressions {
    public static void main(String[] args) {
        // Expressions //
        int score = 100; // Expression is : "score = 100"
        if (score > 99) { // Expression is : "score > 99"
            System.out.println("You got the high score"); // Expression is : " "You got the high score" "
            score = 0; // Expression is : "score =0"
        }

    }
}
